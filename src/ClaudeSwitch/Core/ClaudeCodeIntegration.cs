namespace ClaudeSwitch.Core;

/// <summary>
/// Optional, opt-in hooks into Claude Code itself:
///
///  • a status line that shows which account is active, inside the terminal / editor;
///  • a StopFailure hook that tells the tray the instant a session is rate-limited.
///
/// Both mean writing into the user's ~/.claude/settings.json, which on a real install is mostly
/// a very large "permissions" array. That file is therefore edited the same way ~/.claude.json
/// is: <see cref="JsonSurgeon"/> splices the single root member we own and every other byte is
/// left exactly as it was. The only JSON we ever parse is the small "hooks" value itself.
///
/// Nothing here runs unless the user turns it on, and both can be removed again from the same
/// switch — an integration you cannot uninstall has no business editing someone's config.
/// </summary>
internal static class ClaudeCodeIntegration
{
    internal enum State
    {
        /// <summary>The slot is free.</summary>
        NotInstalled,

        /// <summary>Installed and pointing at our script — safe to remove.</summary>
        Ours,

        /// <summary>Somebody else's. We never touch it.</summary>
        Foreign,
    }

    public static string StatusLineScript => Path.Combine(ClaudePaths.AppDataDir, "statusline.ps1");
    public static string LimitHookScript => Path.Combine(ClaudePaths.AppDataDir, "limit-hook.ps1");

    /// <summary>Where the hook script drops its payloads for the app to pick up.</summary>
    public static string SignalsDir => Path.Combine(ClaudePaths.AppDataDir, "signals");

    /// <summary>
    /// The active account's display name, written by the app and read by the status line script.
    ///
    /// The script deliberately does NOT read ~/.claude.json for this: real config files contain
    /// duplicate keys that make PowerShell's ConvertFrom-Json throw outright.
    /// </summary>
    public static string ActiveLabelFile => Path.Combine(ClaudePaths.AppDataDir, "active.txt");

    public static void WriteActiveLabel(string label)
    {
        try
        {
            ClaudePaths.EnsureAppDirectories();
            AtomicFile.WriteAllText(ActiveLabelFile, label);
        }
        catch (Exception ex) when (ex is IOException or UnauthorizedAccessException)
        {
        }
    }

    // ── status line ──────────────────────────────────────────────────────────

    public static State StatusLineState()
    {
        var settings = ReadSettings();
        if (JsonSurgeon.GetRawValue(settings, "statusLine") is null) return State.NotInstalled;
        return SettingsEditor.IsOurs(settings, "statusLine", SettingsEditor.StatusLineMarker)
            ? State.Ours : State.Foreign;
    }

    public static void InstallStatusLine()
    {
        if (StatusLineState() == State.Foreign)
            throw new InvalidOperationException(
                "Claude Code already has a custom status line configured. Remove it there first — " +
                "ClaudeSwitch won't overwrite something you set up yourself.");

        ClaudePaths.EnsureAppDirectories();
        File.WriteAllText(StatusLineScript, StatusLineScriptBody());

        EditSettings(json => SettingsEditor.SetStatusLine(json, PowerShellCommand(StatusLineScript)));
    }

    public static void RemoveStatusLine()
    {
        if (StatusLineState() != State.Ours) return;
        EditSettings(SettingsEditor.RemoveStatusLine);

        try { File.Delete(StatusLineScript); }
        catch (Exception ex) when (ex is IOException or UnauthorizedAccessException) { }
    }

    /// <summary>
    /// Claude Code hands the script its session JSON on stdin, including live
    /// <c>rate_limits.five_hour.used_percentage</c> — so the percentage shown here is the
    /// session's own, not our polled copy, and costs no API call.
    /// </summary>
    private static string StatusLineScriptBody() => $$"""
        # ClaudeSwitch status line — shows the active account and its live 5-hour usage.
        # Generated by ClaudeSwitch. Remove it from the app's Settings window.
        $ErrorActionPreference = 'SilentlyContinue'

        $payload = [Console]::In.ReadToEnd()

        $label = ''
        try { $label = (Get-Content -LiteralPath '{{ActiveLabelFile}}' -Raw).Trim() } catch { }

        $five = $null
        try { $five = ($payload | ConvertFrom-Json).rate_limits.five_hour.used_percentage } catch { }

        $parts = @()
        if ($label) { $parts += "$([char]0x25CF) $label" }
        if ($null -ne $five) { $parts += ('{0:N0}% 5h' -f [double]$five) }

        if ($parts.Count) { Write-Output ($parts -join ' | ') }
        """;

    // ── rate-limit hook ──────────────────────────────────────────────────────

    /// <summary>Other people's hooks are none of our business — we only ever own our own entry.</summary>
    public static State LimitHookState()
        => SettingsEditor.IsOurs(ReadSettings(), "hooks", SettingsEditor.LimitHookMarker)
            ? State.Ours : State.NotInstalled;

    public static void InstallLimitHook()
    {
        ClaudePaths.EnsureAppDirectories();
        Directory.CreateDirectory(SignalsDir);
        File.WriteAllText(LimitHookScript, LimitHookScriptBody());

        EditSettings(json => SettingsEditor.SetLimitHook(json, PowerShellCommand(LimitHookScript)));
    }

    public static void RemoveLimitHook()
    {
        if (LimitHookState() != State.Ours) return;
        EditSettings(SettingsEditor.RemoveLimitHook);

        try { File.Delete(LimitHookScript); }
        catch (Exception ex) when (ex is IOException or UnauthorizedAccessException) { }
    }

    private static string LimitHookScriptBody() => $$"""
        # ClaudeSwitch rate-limit hook — hands the event to the tray app and exits.
        # Generated by ClaudeSwitch. Remove it from the app's Settings window.
        $ErrorActionPreference = 'SilentlyContinue'

        $payload = [Console]::In.ReadToEnd()
        $dir = '{{SignalsDir}}'

        try {
            if (-not (Test-Path -LiteralPath $dir)) { New-Item -ItemType Directory -Path $dir | Out-Null }
            $file = Join-Path $dir ([guid]::NewGuid().ToString('n') + '.json')
            Set-Content -LiteralPath $file -Value $payload -Encoding UTF8
        } catch { }

        # Always succeed: this runs on an already-failed turn and must not add noise to it.
        exit 0
        """;

    // ── settings.json plumbing ───────────────────────────────────────────────

    /// <summary>The settings file's text, or an empty object when it doesn't exist yet.</summary>
    private static string ReadSettings()
    {
        try
        {
            if (File.Exists(ClaudePaths.ClaudeSettingsFile))
                return File.ReadAllText(ClaudePaths.ClaudeSettingsFile);
        }
        catch (Exception ex) when (ex is IOException or UnauthorizedAccessException)
        {
        }
        return "{}";
    }

    /// <summary>
    /// Applies an edit to settings.json, taking a backup first and writing atomically.
    /// A file that does not exist yet is created as an empty object.
    /// </summary>
    private static void EditSettings(Func<string, string> edit)
    {
        var path = ClaudePaths.ClaudeSettingsFile;
        var json = File.Exists(path) ? File.ReadAllText(path) : "{}";
        if (string.IsNullOrWhiteSpace(json)) json = "{}";

        var updated = edit(json);

        BackupSettings(json);
        Directory.CreateDirectory(Path.GetDirectoryName(path)!);
        AtomicFile.WriteAllText(path, updated);
    }

    private static void BackupSettings(string original)
    {
        try
        {
            ClaudePaths.EnsureAppDirectories();
            var dir = Path.Combine(ClaudePaths.BackupsDir, "claude-settings");
            Directory.CreateDirectory(dir);
            File.WriteAllText(
                Path.Combine(dir, $"settings-{DateTime.Now:yyyyMMdd-HHmmss}.json"), original);
        }
        catch (Exception ex) when (ex is IOException or UnauthorizedAccessException)
        {
            // Best effort — the write below is atomic regardless.
        }
    }

    private static string PowerShellCommand(string scriptPath)
        => $"powershell -NoProfile -ExecutionPolicy Bypass -File \"{scriptPath}\"";
}
